package parttwo.biblioteca;

public class Libro {
    //    - Libro (autore, data pubblicazione, num. pagine, num. volumi, num. capitoli)
    private String autore;
    private String datapub;
    private int numpag;
    private int numvolumi;
    private int numcapitoli;

    public String getAutore() {
        return autore;
    }

    public String getDatapub() {
        return datapub;
    }

    public int getNumpag() {
        return numpag;
    }

    public int getNumvolumi() {
        return numvolumi;
    }

    public int getNumcapitoli() {
        return numcapitoli;
    }
}
